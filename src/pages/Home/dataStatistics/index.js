const Example = (props) => {
  const gps = [];
  const values = Object.values(props.mapData);
  for (let item of values) {
    gps.push([item.lng, item.lat, item.eventId]);
  }
  const [location, setLocation] = useState(gps);

  useEffect(() => {
    const convertResult = [];
    for (let item of location) {
      window.AMap.convertFrom(
        item.slice(0, 2),
        "gps",
        function (state, result) {
          convertResult.push([
            result.locations[0].getLng(),
            result.locations[0].getLat(),
            item[2],
          ]);
          setLocation([...convertResult]);
        }
      );
    }
  }, []);

  return (
    <div>
      {location.map((item, index) => {
        return (
          <Marker
            key={index}
            title=""
            position={item.slice(0, 2)}
            onClick={() => props.show(item)}
          />
        );
      })}
    </div>
  );
};

